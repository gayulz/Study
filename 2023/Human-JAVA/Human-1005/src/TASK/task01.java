package TASK;

public class task01 {
    public static void main(String[] args) {
        for (int i = 1; i <= 4; i++) {
            for (int j = 1; j <= 3 ; j++) {
                System.out.print(i + " * " + j + " = " + i*j + "\t");
            }
            System.out.println(" ");
        }
    }
}
