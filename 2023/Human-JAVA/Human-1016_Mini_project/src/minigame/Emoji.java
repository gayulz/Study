import java.util.Random;

class StrPlot {
    public static final String red = "\u001B[31m";
    public static final String green = "\u001B[32m";
    public static final String yellow = "\u001B[33m";
    public static final String blue = "\u001B[34m";
    public static final String purple = "\u001B[35m";
    public static final String cyan = "\u001B[36m";
    public static final String white = "\u001B[37m";
    public static final String exit = "\u001B[0m";


    public static void Strplot(String emj) {
        String[] color = {red, green, yellow, blue, purple, cyan, white};
        Random r = new Random();
        String colors = color[r.nextInt(5)];
        int timer = 0; // 다음 스트링줄이 나올때까지의 대기시간
        int l = 64; // 스트링 한줄의 길이
        int flag = 0; // stage
        String tmpS = "";
        for (int i = 1; i <= 8; i++) {
            while (flag == 0) {
                for (int j = l * (i - 1); j >= l * (i - 1) && j < l * i; j++) {
                    tmpS += emj.charAt(j); // tmp에 i번째 문자 저장
                }
                System.out.println(colors + tmpS + exit); // out
                tmpS = ""; // Reset
                flag = 1; // loop end
                timer = 6 * 16777216; // next loop까지 대기시간
            }
            while (flag == 1) {
                if (timer == 0) {
                    flag = 0; // loop end
                }
                timer--;
            }
        }
    }
}


